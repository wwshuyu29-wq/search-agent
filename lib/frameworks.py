def get_framework_by_name(name: str) -> dict:
    """根据框架名称获取框架配置"""
    return ANALYSIS_FRAMEWORKS.get(name, None)


def list_all_frameworks() -> list:
    """列出所有可用框架"""
    return [
        {
            "name": framework["name"],
            "description": framework["description"],
            "适用场景": framework["适用场景"]
        }
        for framework in ANALYSIS_FRAMEWORKS.values()
    ]


def get_search_keywords(framework_name: str, **params) -> dict:
    """
    根据框架和用户参数生成搜索关键词

    Args:
        framework_name: 框架名称
        **params: 用户参数,如 主题="高德地图", 公司名="百度", 期间="2026Q1" 等

    Returns:
        dict: {维度名: [关键词列表]}
    """
    framework = ANALYSIS_FRAMEWORKS.get(framework_name)
    if not framework:
        return {}

    template = framework.get("search_keywords_template", {})
    keywords_by_dimension = {}

    for dimension, keyword_templates in template.items():
        keywords = []
        for kw_template in keyword_templates:
            # 替换模板中的占位符
            keyword = kw_template
            for param_key, param_value in params.items():
                keyword = keyword.replace(f"{{{param_key}}}", str(param_value))
            keywords.append(keyword)
        keywords_by_dimension[dimension] = keywords

    return keywords_by_dimension
